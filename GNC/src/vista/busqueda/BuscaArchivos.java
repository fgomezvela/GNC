    public BuscaArchivos(String ruta, String texto, VentanaPrincipal padre) {
        initComponents();
        this.ruta = ruta;
        this.padre = padre;
        if (texto.compareToIgnoreCase("input") == 0) {
            this.jLabel1.setText("Previous Input Networks Loaded");
            this.soyInput = true;
        } else {
            this.jLabel1.setText("Previous Biological Databases Loaded");
            this.soyBD = true;
        }
        this.setLocationRelativeTo(null);
        File f = new File(ruta);
        String[] contenidoDirectorio = f.list();
        this.directorioAux = contenidoDirectorio;
        for (int i = 0; i < contenidoDirectorio.length; i++) {
            contenidoDirectorio[i] = calcularNombre(contenidoDirectorio[i]);
        }
        
        ListModelBuscaArchivos modelo = new ListModelBuscaArchivos(contenidoDirectorio);
        this.jList1.setModel(modelo);
        this.setTitle("GNC - Opening Files");
    }

    private String calcularNombre(String ruta) {
        int ultIndiceBarra = ruta.lastIndexOf(System.getProperty("file.separator"));
        int ultIndicePunto = ruta.lastIndexOf(".");
        String input = ruta.substring(ultIndiceBarra + 1, ultIndicePunto);
        return input;
    }

