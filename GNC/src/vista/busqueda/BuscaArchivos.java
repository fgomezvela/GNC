    private void SelectBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SelectBActionPerformed
        if (soyBD) {
            this.padre.cambiarRutaBD(this.ruta + System.getProperty("file.separator") + this.directorioAux[(this.jList1.getSelectedIndex())] + ".txt");
            this.padre.setRutaFicheroDB(this.ruta + System.getProperty("file.separator") + this.directorioAux[(this.jList1.getSelectedIndex())] + ".txt");
            this.padre.setBbddSeleccionada(true);
        } else if (soyInput) {
            this.padre.cambiarRutaInput(this.ruta + System.getProperty("file.separator") + this.directorioAux[(this.jList1.getSelectedIndex())] + ".txt");
            this.padre.setRutaFicheroInput(this.ruta + System.getProperty("file.separator") + this.directorioAux[(this.jList1.getSelectedIndex())] + ".txt");
            this.padre.setInputSeleccionada(true);
        }
        this.dispose();
    }//GEN-LAST:event_SelectBActionPerformed

